public final class FloatCouple {
    private Float _x;
    private Float _y;

    public FloatCouple(Float x, Float y) {
        _x =x;
        _y = y;
    }

    public Float getX() {
        return _x;
    }

    public Float getY() {
        return _y;
    }
}
